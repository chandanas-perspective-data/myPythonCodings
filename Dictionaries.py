# Dictionary represents a group of elements arranged in the form of key-value pairs.
# In the Dictionary ,the first element is considered as 'key' and the immediate next element is taken as its 'value'.
# All the key-value pairs in a dictionary are inserted in curly braces { }.
## A Python program to create a dictionary with  employee details  and retrive the values upon giving the keys.
 

